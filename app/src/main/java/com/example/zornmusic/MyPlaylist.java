package com.example.zornmusic;

class MyPlaylist {


    String plName;
    int image;


    public MyPlaylist(String plName, int image) {
        this.plName = plName;
        this.image = image;
    }

    public String getPlName() {
        return plName;
    }

    public int getImage() {
        return image;
    }
}
